        #region "constructor"
        public DFSContext(DbContextOptions<DFSContext> options) : base(options) { }
        #endregion

        #region "overrides"
        protected override void OnModelCreating(ModelBuilder modelBuilder)
        {
            // Ensure all keys are generated by database
            modelBuilder.UseIdentityAlwaysColumns();

            // Ensure all CreatedAt and UpdatedAt timestamps are generated by database
            foreach (IMutableEntityType entityType in modelBuilder.Model.GetEntityTypes())
            {
                IMutableProperty? createdAt = entityType.FindProperty("CreatedAt");
                if (createdAt != null)
                {
                    createdAt.SetDefaultValueSql("now()");
                }

                // TODO: Look into setting UpdatedAt automagically with DB trigger
                IMutableProperty? updatedAt = entityType.FindProperty("UpdatedAt");
                if (updatedAt != null)
                {
                    updatedAt.SetDefaultValueSql("now()");
                }
            }

            modelBuilder.Entity<Kit>()
                .HasMany<WeaponCategory>(e => e.WeaponCategories)
                .WithMany(e => e.Kits)
                .UsingEntity("KitWeaponCategory",
                    l => l.HasOne(typeof(WeaponCategory)).WithMany().HasForeignKey("WeaponCategoryId"),
                    r => r.HasOne(typeof(Kit)).WithMany().HasForeignKey("KitId"))
                .HasKey("KitId", "WeaponCategoryId");

            modelBuilder.Entity<Kit>()
                .HasMany<ArmorCategory>(e => e.ArmorCategories)
                .WithMany(e => e.Kits)
                .UsingEntity("KitArmorCategory",
                    l => l.HasOne(typeof(ArmorCategory)).WithMany().HasForeignKey("ArmorCategoryId"),
                    r => r.HasOne(typeof(Kit)).WithMany().HasForeignKey("KitId"))
                .HasKey("KitId", "ArmorCategoryId");

            modelBuilder.Entity<SkillOption>()
                .HasMany(e => e.SkillPools)
                .WithMany(e => e.SkillOptions)
                .UsingEntity<SkillOptionPool>();

            modelBuilder.Entity<SkillPool>()
               .HasMany(e => e.SkillGroups)
               .WithMany(e => e.SkillPools)
               .UsingEntity("SkillPoolSkillGroup",
                   l => l.HasOne(typeof(SkillGroup)).WithMany().HasForeignKey("SkillGroupId"),
                   r => r.HasOne(typeof(SkillPool)).WithMany().HasForeignKey("SkillPoolId"))
               .HasKey("SkillPoolId", "SkillGroupId");

            modelBuilder.Entity<SkillPool>()
                .HasMany(e => e.Skills)
                .WithMany(e => e.SkillPools)
                .UsingEntity("SkillPoolSkill",
                    l => l.HasOne(typeof(Skill)).WithMany().HasForeignKey("SkillId"),
                    r => r.HasOne(typeof(SkillPool)).WithMany().HasForeignKey("SkillPoolId"))
                .HasKey("SkillPoolId", "SkillId");

            SeedCharacteristic(modelBuilder);

            SeedHeroLevelEchelon(modelBuilder);
            SeedHeroLevel(modelBuilder);

            SeedAncestry(modelBuilder);
            SeedSignatureAncestryTrait(modelBuilder);
            SeedPurchasedAncestryTrait(modelBuilder);

            SeedCulturePreset(modelBuilder);
            SeedEnvironment(modelBuilder);
            SeedOrganization(modelBuilder);
            SeedUpbringing(modelBuilder);
            SeedLanguage(modelBuilder);

            SeedCareer(modelBuilder);
            SeedCareerQuestion(modelBuilder);
            SeedIncitingIncident(modelBuilder);

            SeedPerkType(modelBuilder);
            SeedPerk(modelBuilder);

            SeedSkillGroup(modelBuilder);
            SeedSkill(modelBuilder); ;
            SeedSkillOption(modelBuilder);
            SeedSkillPool(modelBuilder);
            SeedSkillOptionPool(modelBuilder);
            SeedSkillPoolSkillGroup(modelBuilder);
            SeedSkillPoolSkill(modelBuilder);

            SeedHeroClass(modelBuilder);

            SeedWeaponCategory(modelBuilder);
            SeedArmorCategory(modelBuilder);
            SeedKit(modelBuilder);
            SeedKitWeaponCategory(modelBuilder);
            SeedKitArmorCategory(modelBuilder);

            base.OnModelCreating(modelBuilder);
        }
        protected override void OnConfiguring(DbContextOptionsBuilder optionsBuilder)
        {
            optionsBuilder.UseSnakeCaseNamingConvention();
            optionsBuilder.EnableSensitiveDataLogging();
            base.OnConfiguring(optionsBuilder);
        }
